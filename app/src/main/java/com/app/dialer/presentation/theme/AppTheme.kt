/**
 * Root theme composable for the Dialer app.
 *
 * Blends Material3 theming with the neumorphic and bento visual language.
 * - On API 31+ devices, dynamic color (Material You) is supported when [dynamicColor] = true.
 * - On API 26–30, falls back to the static branded palette.
 * - Exposes [LocalNeumorphicColors] for neumorphic shadow rendering throughout the tree.
 *
 * @param darkTheme      Whether to use the dark color scheme. Defaults to system setting.
 * @param dynamicColor   Whether to apply dynamic Material You colors (API 31+ only).
 * @param content        The composable content tree.
 */
@Composable
fun AppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    val context = LocalContext.current

    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    val neumorphicColors = if (darkTheme) DarkNeumorphicColors else LightNeumorphicColors

    CompositionLocalProvider(
        LocalNeumorphicColors provides neumorphicColors
    ) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = DialerTypography,
            shapes = DialerShapes,
            content = content
        )
    }
}
